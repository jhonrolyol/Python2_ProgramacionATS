'''
1.- Se utilizan para establecer una relación entre 2 valores.
2.- Compara estos valores entre si y la comparación produce un
    resultado de certeza o falsedad (verdadero o falso).
3.- Tienen el mismo nivel de prioridad en su evaluación.
4.- Los operadores relacionales tienen menor que los aritméticos.
'''

'''
Operadores relacionales:
   <  Menor que
   >  Mayor que
   >= Mayor o igual que
   <= Menor o igual query
   != Diferente
   == Igual
  Note: Son utiles para comparar dos valores.
'''

# Aplicación
resultado1  = 10 < 20
print(resultado1)

resultado2 = 10  > 20
print(resultado2)

resultado3  = 10 == 20
print(resultado3)

resultado4 = 10 != 20
print(resultado4)

resultado5 = 10 >= 20
print(resultado5)

resultado6 = 10 <= 20
print(resultado6)

# Suma
a = 10
b = 20
c = 30

resultado7 = a + b == c
print(resultado7)

resultado8 = a + b != c
print(resultado8)

resultado9 = a + b >= c
print(resultado9)

resultado10 = a + b <= c
print(resultado10)

